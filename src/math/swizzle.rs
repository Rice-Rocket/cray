macro_rules! impl_swizzle {
    ($($ty:ident: $($name:ident -> $out:ident[$($i:expr),+]),* $(,)*);* $(;)*) => {
        $(
            impl<T: Clone + Copy> $ty<T> {
                $(
                    #[inline]
                    pub fn $name(self) -> $out<T> {
                        $out::new($(
                            self[$i],
                        )+)
                    }
                )*
            }
        )*
    }
}

